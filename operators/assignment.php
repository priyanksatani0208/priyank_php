<h1><u>Assignment(=) operator</u></h1>

<?php
  #equal operator
  $a=45;
  echo "<h2>A is := $a</h2>";
  
  #plus equal operator
  $a=45;
  $b=50;
  
  $a+=$b;
  echo "<h2>Pulse equal operator := $a</h2>";
  
  #minus equal operator
  $a=60;
  $b=50;
  
  $a-=$b;
  echo "<h2>minus equal operator := $a</h2>";
  
  #multipliy equal operator
  $a=40;
  $b=50;
  
  $a*=$b;
  echo "<h2>multipliy equal operator := $a</h2>";
  
  #division equal operator
  $a=40;
  $b=5;
  
  $a/=$b;
  echo "<h2>Division equal operator := $a</h2>";
  
  #modulo equal operator
  $a=41;
  $b=5;
  
  $a%=$b;
  echo "<h2>Modulo equal operator := $a</h2>";
  
?>
